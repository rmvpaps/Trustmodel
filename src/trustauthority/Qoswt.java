package trustauthority;

public class Qoswt {
	
	String qos;
	int val;
	
	public Qoswt()
	{
		super();
	}
	
	public Qoswt(String qos,int val) {
		super();
		this.qos = qos;
		this.val = val;
	}
	
	public String getQos() {
		return qos;
	}
	public void setQos(String qos) {
		this.qos = qos;
	}
	public int getVal() {
		return val;
	}
	public void setVal(int val) {
		this.val = val;
	}
	

}
