package trustauthority;

public class TrustResult {
	
	DT_sp dt;
	double indirect_trust;
	double overall_trust;
	public DT_sp getDt() {
		return dt;
	}
	public void setDt(DT_sp dt) {
		this.dt = dt;
	}
	public double getIndirect_trust() {
		return indirect_trust;
	}
	public void setIndirect_trust(double indirect_trust) {
		this.indirect_trust = indirect_trust;
	}
	public double getOverall_trust() {
		return overall_trust;
	}
	public void setOverall_trust(double overall_trust) {
		this.overall_trust = overall_trust;
	}
	
	

}
